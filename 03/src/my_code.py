# -*- coding: utf-8 -*-

first_value = input("Give 1st number:")
second_value = input("Give 2nd number:")

print(f"Sum:    {first_value} + {second_value} = {int(first_value) + int(second_value)}")
print(f"Difference:    {first_value} - {second_value} = {int(first_value) - int(second_value)}")
print(f"Product:    {first_value} * {second_value} = {int(first_value) * int(second_value)}")